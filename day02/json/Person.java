package json;

public class Person {
	String name;
	PC notebook;
	School School;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public PC getNotebook() {
		return notebook;
	}
	public void setNotebook(PC notebook) {
		this.notebook = notebook;
	}
	public School getSchool() {
		return School;
	}
	public void setSchool(School school) {
		School = school;
	}
	
	
}
